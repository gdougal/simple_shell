#include "libft.h"

size_t	ft_strchr_len(char *s, int c)
{
	size_t	i;

	i = 0;
	while (*(s + i) != c)
	{
		if (*(s + i) == '\0')
			return (-1);
		i++;
	}
	return (i);
}
