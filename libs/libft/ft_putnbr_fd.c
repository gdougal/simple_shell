#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		num;
	int			i;
	long int	j;
	int			sign;

	i = n;
	j = 10;
	sign = n < 0 ? -1 : 1;
	while (i /= 10)
		j *= 10;
	if (sign < 0)
		write(fd, "-", 1);
	while (j /= 10)
	{
		num = ((n / j) * sign) + '0';
		write(fd, &num, 1);
		n %= j;
	}
}
